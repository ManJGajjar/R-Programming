#Arithmetic Operators in R

a <- 10
b <- 20

print(a + b) # Addition
print(a - b) # Subtraction
print(a * b) # Multiplication
print(a / b) # Division
print(a %% b) # Modulus
print(a %/% b) # Integer Division
print(a ^ b) # Exponentiation

